"""
trigonometry.py
====================================
Module for trigonometry related calculations.
"""

class Trigonometry:
    """A class that helps you with advanced calculations."""

    def sin(self, x):
        """
        Returns the sine of a number.

        :param kind: input number.
        :type kind: float
        :return: the sine of a number.
        :rtype: float.
        """
        import numpy as np
        return np.sin(x)

    def cos(self, x):
        """
        Returns the cosine of a number.

        :param kind: input number.
        :type kind: float
        :return: the cosine of a number.
        :rtype: float.
        """
        import numpy as np
        return np.cos(x)

    def tan(self, x):
        """
        Returns the tangent of a number.

        :param kind: input number.
        :type kind: float
        :return: the tangent of a number.
        :rtype: float.
        """
        import numpy as np
        return np.tan(x)